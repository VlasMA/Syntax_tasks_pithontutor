# несмещенное стандартное отклонение и дисперсию для выборки y
import numpy as np
import numpy

y = np.array ([100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150])

print(np.std(y, ddof=1))
# 31.624607341019814
print(np.var(y, ddof=1))
# 1000.1157894736842
print(np.sqrt(1000.1157894736842))
# 31.624607341019814